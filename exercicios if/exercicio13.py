data = int(input('Digite entre os números 1 e 7: '))
if data == 1:
    print(f'{data} - Domingo')
elif data == 2:
    print(f'{data} - Segunda')
elif data == 3:
    print(f'{data} - Terça')
elif data == 4:
    print(f'{data} - Quarta')
elif data == 5:
    print(f'{data} - Quinta')
elif data == 6:
    print(f'{data} - Sexta')
elif data == 7:
    print(f'{data} - Ssabado')
else:
    print('Número errado')